// Hook for using translations
export function useTranslation(language: string = 'tr') {
  const t = (key: string): string => {
    const lang = translations[language as keyof typeof translations] || translations.tr
    return lang[key as keyof typeof lang] || key
  }
  
  return { t, language }
}

